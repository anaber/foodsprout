<h4>Mission</h4>
Our mission is simple: to map the world's food chain and empower you to know what's in your food.
<br>
<br>
<h4>Goals</h4>
Our goals for Food Sprout are vast:<br>
- To help restaurants and food companies become more sustainable on how they produce food<br>
- To provide all the knowledge for people to make decisions about their food<br>
- Help give people of the world access to high nutritional food<br><br>
